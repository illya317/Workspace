import fs from "fs/promises";
import path from "path";
import crypto from "crypto";
import { prisma } from "@/lib/prisma";
import { safeResolve, getDefaultRoot } from "@/server/services/library/config";
import type { GeneratorOutput } from "./types";

export interface GeneratedDocumentInput {
  generatorKey: string;
  title: string;
  summary?: string;
  confidentialityLevel: number;
  categoryCode?: string;
  categoryName?: string;
  userId: number;
}

export async function upsertGeneratedDocument(
  input: GeneratedDocumentInput,
  output: GeneratorOutput,
): Promise<{ document: { id: number; stableKey: string }; isNewVersion: boolean }> {
  const root = getDefaultRoot();
  if (!root) throw new Error("LIBRARY_ROOT not configured");

  const dirSlug = sanitizePathSegment(input.generatorKey);
  const titleSlug = slug(input.title);
  const titleHash = crypto.createHash("sha256").update(input.title).digest("hex").slice(0, 8);
  const baseName = `${titleSlug || "untitled"}-${titleHash}`;
  const fileName = `${baseName}.${output.extension}`;
  const fileSlug = sanitizePathSegment(fileName);
  const relativePath = path.join("generated", dirSlug, fileSlug);
  const absPath = safeResolve(relativePath, root);
  if (!absPath) throw new Error("Invalid generated file path");

  // Ensure directory exists
  await fs.mkdir(path.dirname(absPath), { recursive: true });

  const content = Buffer.isBuffer(output.content) ? output.content : Buffer.from(output.content, "utf-8");
  await fs.writeFile(absPath, content);

  const stats = await fs.stat(absPath);
  const checksum = crypto.createHash("sha256").update(content).digest("hex");
  const stableKey = `generated:${input.generatorKey}:${baseName}`;

  const now = new Date();

  // Upsert LibraryDocument
  const doc = await prisma.libraryDocument.upsert({
    where: { stableKey },
    create: {
      stableKey,
      rootKey: "default",
      relativePath,
      fileName,
      extension: output.extension,
      mimeType: output.mimeType,
      fileSizeBytes: stats.size,
      fileMtime: stats.mtime,
      checksumSha256: checksum,
      title: input.title,
      summary: input.summary ?? null,
      categoryCode: input.categoryCode ?? null,
      categoryName: input.categoryName ?? null,
      confidentialityLevel: input.confidentialityLevel,
      status: "active",
      origin: "generated",
      generatorKey: input.generatorKey,
      editedBy: input.userId,
      editedAt: now,
    },
    update: {
      relativePath,
      fileName,
      extension: output.extension,
      mimeType: output.mimeType,
      fileSizeBytes: stats.size,
      fileMtime: stats.mtime,
      checksumSha256: checksum,
      title: input.title,
      summary: input.summary ?? null,
      categoryCode: input.categoryCode ?? null,
      categoryName: input.categoryName ?? null,
      confidentialityLevel: input.confidentialityLevel,
      status: "active",
      editedBy: input.userId,
      editedAt: now,
    },
  });

  // Check if content changed by comparing last version checksum
  const lastVersion = await prisma.libraryDocumentVersion.findFirst({
    where: { documentId: doc.id },
    orderBy: { versionNo: "desc" },
  });

  const isNewVersion = !lastVersion || lastVersion.checksumSha256 !== checksum;

  if (isNewVersion) {
    const nextVersionNo = (lastVersion?.versionNo ?? 0) + 1;
    await prisma.libraryDocumentVersion.create({
      data: {
        documentId: doc.id,
        versionNo: nextVersionNo,
        relativePath,
        fileSizeBytes: stats.size,
        fileMtime: stats.mtime,
        checksumSha256: checksum,
        changeNote: `Generated by ${input.generatorKey}`,
        createdBy: input.userId,
      },
    });

    // Bump document version counter
    await prisma.libraryDocument.update({
      where: { id: doc.id },
      data: { version: { increment: 1 } },
    });
  }

  return { document: { id: doc.id, stableKey: doc.stableKey }, isNewVersion };
}

function sanitizePathSegment(s: string): string {
  return s
    .replace(/\\/g, "-")
    .replace(/\//g, "-")
    .replace(/\.{2,}/g, "-")
    .replace(/[^\w一-龥.-]/g, "")
    .slice(0, 60);
}

function slug(text: string): string {
  return text
    .trim()
    .replace(/\s+/g, "-")
    .replace(/[^\w一-龥-]/g, "")
    .slice(0, 50);
}
